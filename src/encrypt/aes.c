static const Byte _mixm[16] = {
    2, 1, 1, 3,
    3, 2, 1, 1,
    1, 3, 2, 1,
    1, 1, 3, 2
};

static const Byte _imixm[16] = {
    0xe, 0x9, 0xd, 0xb,
    0xb, 0xe, 0x9, 0xd,
    0xd, 0xb, 0xe, 0x9,
    0x9, 0xd, 0xb, 0xe
};

static const Byte _rcon[11] = {
    0, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36
};

static Byte _rijmul(Byte a, Byte b) {
    if (a > b) {
        a ^= b;
        b ^= a;
        a ^= b;
    };
    if (b == 0) return 0;
    Byte res = 0;
    for (Byte p = b; a;) {
        if (a & 1) res ^= p;
        a >>= 1;
        if (p >> 7) p = (p << 1) ^ 0x1b;
        else p <<= 1;
    }
    return res;
}

static void _mmul(Byte *res, const Byte *am, const Byte *bm) {
    memset(res, 0, 16);
    for (int i = 0; i < 4; i++)
        for (int j = 0; j < 4; j++)
            for (int k = 0; k < 4; k++)
                res[i * 4 + k] ^= _rijmul(am[i * 4 + j], bm[j * 4 + k]);
}

static void _bsub(Byte *block) {
    for (int i = 0; i < 16; i++) {
        block[i] = _sbox[block[i] >> 4][block[i] & 0xf];
    }
}

static void _bisub(Byte *block) {
    for (int i = 0; i < 16; i++) {
        block[i] = _isbox[block[i] >> 4][block[i] & 0xf];
    }
}

static void _bshift(Byte *block) {
    Byte temp[4];
    for (int col = 1; col < 4; col++) {
        for (int i = 0; i < 4; i++)
            temp[i] = block[(i + col) % 4 * 4 + col];
        for (int i = 0; i < 4; i++)
            block[i * 4 + col] = temp[i];
    }
}

static void _bishift(Byte *block) {
    Byte temp[4];
    for (int col = 1; col < 4; col++) {
        for (int i = 0; i < 4; i++)
            temp[i] = block[(i - col + 4) % 4 * 4 + col];
        for (int i = 0; i < 4; i++)
            block[i * 4 + col] = temp[i];
    }
}

static void _bmix(Byte *block) {
    Byte temp[16];
    _mmul(temp, block, _mixm);
    memcpy(block, temp, 16);
}

static void _bimix(Byte *block) {
    Byte temp[16];
    _mmul(temp, block, _imixm);
    memcpy(block, temp, 16);
}

static void _bxor(Byte *block, const Byte *block2) {
    for (int i = 0; i < 16; i++)
        block[i] ^= block2[i];
}

static void _extkey(Byte keymat[][16]) {
    Byte temp[4];
    for (int round = 1; round <= 10; round++) {
        for (int i = 0; i < 4; i++)
            temp[i] = keymat[round - 1][(i + 1) % 4 + 12];
        for (int i = 0; i < 4; i++)
            temp[i] = _sbox[temp[i] >> 4][temp[i] & 0xf];
        temp[0] ^= _rcon[round];

        for (int i = 0; i < 4; i++)
            keymat[round][i] = keymat[round - 1][i] ^ temp[i];
        for (int i = 4; i < 16; i++)
            keymat[round][i] = keymat[round - 1][i] ^ keymat[round][i - 4];
    }
}

static void _aes128(Byte *block, const Byte keymat[][16]) {
    for (int round = 0; round < 10; round++) {
        _bxor(block, keymat[round]);
        _bsub(block);
        _bshift(block);
        if (round != 9) _bmix(block);
        else _bxor(block, keymat[10]);
    }
}

static void _deaes128(Byte *block, const Byte keymat[][16]) {
    for (int round = 9; round >= 0; round--) {
        if (round != 9) _bimix(block);
        else _bxor(block, keymat[10]);
        _bishift(block);
        _bisub(block);
        _bxor(block, keymat[round]);
    }
}

size_t aes128cbc_chiperlen(size_t plainlen) {
    return plainlen / 16 * 16 + 32;
}

void aes128cbc_encrypt(Byte *chiper, const Byte *plain, size_t len, const Byte *key, Byte(*randgen)()) {
    // iv
    for (int i = 0; i < 16; i++)
        chiper[i] = randgen();
    // key
    Byte keymat[11][16];
    memcpy(keymat, key, 16);
    _extkey(keymat);
    // nblocks - 1 round
    for (size_t n = 1; n <= len / 16; n++) {
        Byte *dst = n * 16 + chiper;
        const Byte *src = (n - 1) * 16 + plain;
        for (int i = 0; i < 16; i++)
            dst[i] = src[i] ^ dst[i - 16];
        _aes128(dst, keymat);
    }
    // last round
    size_t before = len / 16 * 16;
    const Byte *src = plain + before;
    Byte *dst = chiper + before + 16;
    size_t remain = len - before;
    Byte padding = 16 - remain;
    for (size_t i = 0; i < remain; i++)
        dst[i] = src[i] ^ dst[i - 16];
    for (size_t i = remain; i < 16; i++)
        dst[i] = padding ^ dst[i - 16];
    _aes128(dst, keymat);
}

size_t aes128cbc_decrypt(Byte *plain, const Byte *chiper, size_t len, const Byte *key) {
    if (len % 16) return SIZE_MAX;
    // key
    Byte keymat[11][16];
    memcpy(keymat, key, 16);
    _extkey(keymat);
    // copy
    memcpy(plain, chiper + 16, len - 16);
    // decrypt
    for (size_t i = 0; i < len / 16 - 1; i++) {
        Byte *p1 = i * 16 + plain;
        const Byte *p2 = i * 16 + chiper;
        _deaes128(p1, keymat);
        _bxor(p1, p2);
    }
    // check padding
    size_t tail = len - 16 - 1;
    Byte padding = plain[tail];
    if (!(padding >= 1 && padding <= 16))
        return SIZE_MAX;
    for (int i = 1; i < padding; i++)
        if (plain[--tail] != padding)
            return SIZE_MAX;
    return tail;
}
